export const makeMyTeams = matches.reduce(function (accumulator, currentValue) {
  let teamName: string[] = currentValue.MATCH.split("-");
  if (checkIfValid(teamName) && accumulator.indexOf(teamName[0].trim()) < 0)
    return [...accumulator, teamName[0].trim()];
  else return [...accumulator];
}, new Array());

